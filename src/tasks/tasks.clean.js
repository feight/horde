module.exports = function(grunt){


    /* -------------------------------------------------------------------- */
    /*
            requires
    */
    /* -------------------------------------------------------------------- */


    var utils = require(require("path").resolve("horde/src/utils/utils.js"))(grunt);


    /* -------------------------------------------------------------------- */
    /*
            public
    */
    /* -------------------------------------------------------------------- */


    var clean = function(paths){

        for(var i = 0; i < paths.length; i++){
            utils.execSync("rm -rf {0}".format(paths[i]));
        }

    };

    return clean;

};